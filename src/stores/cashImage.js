import axios from "axios";
import { defineStore } from "pinia";

export const useCashImageStore = defineStore("cashImage", {
  state: () => ({
    cashImages: null,
    cashAccounts: null,
    loading: false,
    loadingCash: false,
  }),
  getters: {},
  actions: {
    async getSimpleListAction(params) {
      try {
        this.loading = true;
        const response = await axios.get("/cash-images");
        this.cashImages = response.data.result;
        this.loading = false;

        return response.data;
      } catch (error) {
        this.loading = false;
        throw error;
      }
    },
    async getChangePage(url, params) {
      this.loading = true;
      const response = await axios.get(url, {
        params: params,
      });
      this.cashImages = response.data.result;
      this.loading = false;
      return response.data;
    },
    async getListAction(params) {
      try {
        this.loading = true;
        const response = await axios.get("/cash-images", {
          params: params,
        });

        this.loading = false;
        this.cashImages = response.data.result;
        console.log(response);
        return response.data;
      } catch (error) {
        this.loading = false;
        throw error;
      }
    },
    async getSummary(params) {
      try {
        this.loadingCash = true;
        const response = await axios.get("/summary-report", { params: params });
        this.cashAccounts = response.data.result;
        console.log(response, "this is response for summary");
        return response.data;
      } catch (error) {
        this.loadingCash = false;
      } finally {
        this.loadingCash = false;
      }
    },
    async getChangeSummaryPage(url, params) {
      this.loadingCash = true;
      const response = await axios.get(url, {
        params: params,
      });
      this.cashAccounts = response.data.result;
      this.loadingCash = false;
      return response.data;
    },
    async addNewAction(data) {
      try {
        const response = await axios.post("/cash-images", data);
        return response.data;
      } catch (error) {
        throw error;
      }
    },
    async getDetailAction(id) {
      try {
        const response = await axios.get("/cash-images/" + id);
        return response.data;
      } catch (error) {
        throw error;
      }
    },
    async updateAction(data, id) {
      try {
        const response = await axios.post("/cash-images/" + id, data);
        return response.data;
      } catch (error) {
        throw error;
      }
    },
    async deleteAction(id) {
      try {
        const response = await axios.delete("/cash-images/" + id);
        return response.data;
      } catch (error) {
        throw error;
      }
    },

    async getVatSummary(params) {
      try {
        this.loading = true;
        const response = await axios.get("/summary-report-vat", {
          params: params,
        });
        console.log(response);

        // this.cashImages = response.data.result;/
        this.loading = false;
        console.log(response);
        return response.data;
      } catch (error) {
        this.loading = false;
        throw error;
      }
    },
    async exportCsv(params) {
      try {
        this.loading = true;
        const response = await axios.get("/summary/export-csv", {
          params: params,
        });
        this.loading = false;
        return response.data;
      } catch (error) {
        this.loading = false;
        throw error;
      }
    },
    async printImage(params) {
      try {
        this.loading = true;
        const response = await axios.get("/print/cash-image", {
          params: params,
        });

        console.log(response);

        this.loading = false;
        return response.data;
      } catch (error) {
        this.loading = false;
        throw error;
      }
    },
  },
});
